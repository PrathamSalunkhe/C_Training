//example on ifelse statement

#include <stdio.h>

int main()
{
    int a = 5;
    if(a != 9)
    {
        //if cond. satisfies then this will be o/p
        printf("a is 5\n");  
    }
    else
    {
        //if cond does not satisfies then this will be o/p
        printf("a is not 5\n");   
    }
    return 0;
}